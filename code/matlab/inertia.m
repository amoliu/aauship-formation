m2 = 0.750;
m3 = 1.060;
m4 = 0.650;
m5 = 0.800;
m6 = 6.240;
x1 = 0.490;
y1 = 0;
z1 = -0.05;
x2 = 0.290;
y2 = 0;
z2 = z1;
x3 = 0.100;
y3 = 0;
z3 = z1;
x4 = 0.095;
y4 = 0;
z4 = z1;
x5 = 0.300;
y5 = 0;
z5 = z1;
