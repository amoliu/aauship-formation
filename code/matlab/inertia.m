% Dimensions for the weights
h = 0.01;
w = 0.10;
d = 0.10;


Ix1 = 1/12 * m1 * ( h^2 + d^2);
Iy1 = 1/12 * m1 * ( w^2 + d^2);
Iz1 = 1/12 * m1 * ( w^2 + h^2);

Ix2 = 1/12 * m2 * ( h^2 + d^2);
Iy2 = 1/12 * m2 * ( w^2 + d^2);
Iz2 = 1/12 * m2 * ( w^2 + h^2);

Ix3 = 1/12 * m3 * ( h^2 + d^2);
Iy3 = 1/12 * m3 * ( w^2 + d^2);
Iz3 = 1/12 * m3 * ( w^2 + h^2);

Ix4 = 1/12 * m4 * ( h^2 + d^2);
Iy4 = 1/12 * m4 * ( w^2 + d^2);
Iz4 = 1/12 * m4 * ( w^2 + h^2);

Ix5 = 1/12 * m5 * ( h^2 + d^2);
Iy5 = 1/12 * m5 * ( w^2 + d^2);
Iz5 = 1/12 * m5 * ( w^2 + h^2);

Ixx = Ix1 + m1*(y1^2+z1^2) ...
    + Ix2 + m2*(y2^2+z2^2) ...
    + Ix3 + m3*(y3^2+z3^2) ...
    + Ix4 + m4*(y4^2+z4^2) ...
    + Ix5 + m5*(y5^2+z5^2)

Iyy = Iy1 + m1*(x1^2+z1^2) ...
    + Iy2 + m2*(x2^2+z2^2) ...
    + Iy3 + m3*(x3^2+z3^2) ...
    + Iy4 + m4*(x4^2+z4^2) ...
    + Iy5 + m5*(x5^2+z5^2)

Izz = Iz1 + m1*(x1^2+y1^2) ...
    + Iz2 + m2*(x2^2+y2^2) ...
    + Iz3 + m3*(x3^2+y3^2) ...
    + Iz4 + m4*(x4^2+y4^2) ...
    + Iz5 + m5*(x5^2+y5^2)

Ixy = 0 + m1*x1*y1 ...
    + 0 + m2*x2*y2 ...
    + 0 + m3*x3*y3 ...
    + 0 + m4*x4*y4 ...
    + 0 + m5*x5*y5

Ixz = 0 + m1*x1*z1 ...
    + 0 + m2*x2*z2 ...
    + 0 + m3*x3*z3 ...
    + 0 + m4*x4*z4 ...
    + 0 + m5*x5*z5 ...
    
Iyz = 0 + m1*y1*z1 ...
    + 0 + m2*y2*z2 ...
    + 0 + m3*y3*z3 ...
    + 0 + m4*y4*z4 ...
    + 0 + m5*y5*z5 ...
    

I = [Ixx Ixy Ixz; Ixy Iyy Iyz; Ixz Iyz Izz]
